package Logic;

public class Index {

    private int ROW;
    private int COL;

    public int getROW() {
        return ROW;
    }

    public int getCOL() {
        return COL;
    }

    public Index setROW(int ROW) {
        this.ROW = ROW;
        return this;
    }

    public Index setCOL(int COL) {
        this.COL = COL;
        return this;
    }
}
